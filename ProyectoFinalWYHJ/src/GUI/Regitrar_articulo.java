    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        if(txtcodigo.getText().equals("") || txtnombre.getText().equals("") || txtcategoria.getText().equals("") || txtprecio.getText().equals("") || txtcat.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Para registrar un articulo necesita llenar todos los campos");
        }
        else{
          Conectar c = new Conectar();
            c.getConecxion();
            c.insertaArticulo(Integer.parseInt(txtcodigo.getText()),txtnombre.getText(), txtcategoria.getText(),Long.parseLong(txtprecio.getText()), Integer.parseInt(txtcat.getText()));
            limpiar();
        }
    }//GEN-LAST:event_btnregistrarActionPerformed

    private void txtcodigoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcodigoKeyTyped
        int tecla=evt.getKeyChar();
        boolean numeros=tecla>=48 && tecla<=57;
        if(!numeros){
            evt.consume();
        }
    }//GEN-LAST:event_txtcodigoKeyTyped

    private void txtnombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtnombreKeyTyped
        char tecla=evt.getKeyChar();
        boolean mayus=tecla>=65 && tecla<=90;
        boolean minus=tecla>=97 && tecla<=122;
        boolean espacio=tecla==32;
        if(!(mayus || minus || espacio)){
            evt.consume();
        }
    }//GEN-LAST:event_txtnombreKeyTyped

