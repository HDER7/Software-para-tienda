    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        if(txtcodigo.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Para buscar un articulo debe ingresar el codigo del articulo");
        }
        else{
            Conectar con =new Conectar();
            con.getConecxion();
            con.buscarArticulo(Integer.parseInt(txtcodigo.getText()));
            txtnombre.setText(con.getNomArticulo());
            txtcategoria.setText(con.getCatArticulo());
            txtprecio.setText(String.valueOf(con.getPrecio()));
            txtcat.setText(String.valueOf(con.getCantidad()));
        }
        
    }//GEN-LAST:event_btnbuscarActionPerformed

