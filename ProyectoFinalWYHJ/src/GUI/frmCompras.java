    private void txtFechaCompraKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFechaCompraKeyTyped
        int tecla=evt.getKeyChar();
        boolean numeros=tecla>=48 && tecla<=57 || tecla==45;
        if(!numeros){
            evt.consume();
        }
        if(txtFechaCompra.getText().length()>=10){
            evt.consume();
        }
    }//GEN-LAST:event_txtFechaCompraKeyTyped

    private void formInternalFrameActivated(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameActivated
        DateTimeFormatter fecha=DateTimeFormatter.ofPattern("yyyy-MM-dd");  //HORA: HH--MM--SS
        DateTimeFormatter hora=DateTimeFormatter.ofPattern("HH:MM:SS");
        txtFechaCompra.setText(fecha.format(LocalDateTime.now()));
        txtHora.setText(hora.format(LocalDateTime.now()));
    }//GEN-LAST:event_formInternalFrameActivated

    private void btnBuscarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCActionPerformed
        if(txtCodigoCliente.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Para buscar un empleado debe ingresar el codigo del empleado");
        }
        else{
            Conectar con =new Conectar();
            con.getConecxion();
            con.buscarCliente(Integer.parseInt(txtCodigoCliente.getText()));
            txtCodigoCliente.setText(txtCodigoCliente.getText());
            txtNomCliente.setText(con.getNomCliente()+" "+con.getApeCliente());
            txtEdad.setText(String.valueOf(con.getEdadCliente()));
            con.bodega(Integer.parseInt(txtCodigoArticulo.getText()));
            lblCantidadBodega.setText(String.valueOf(con.getCantidad()));
            txtPrecioUnidad.setText(String.valueOf(con.getPrecio()));
            con.buscarArticulo(Integer.parseInt(txtCodigoArticulo.getText()));
            txtNomArticulo.setText(con.getNomArticulo());
            
        }
    }//GEN-LAST:event_btnBuscarCActionPerformed

