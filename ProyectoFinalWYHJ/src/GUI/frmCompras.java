    private void BtnregistrarcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnregistrarcActionPerformed

          if(txtCodigoArticulo.getText().equals("")|| txtCodigoCliente.getText().equals("")|| txtPrecioUnidad.getText().equals("") || txtEdad.getText().equals("") || txtNomCliente.getText().equals("") || txtNomArticulo.getText().equals("")|| txtCuantoLlevara.getText().equals("")|| txtCodigoE.getText().equals("")|| txtNombreE.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Complete todos los campos para registrar el empleado");
        }
        else{
            Conectar con =new Conectar();
            con.getConecxion();
            int to = Integer.parseInt(txtCuantoLlevara.getText())*(con.getPrecio());
            con.Ventas(Integer.parseInt(txtCodigoArticulo.getText()), Integer.parseInt(txtCodigoE.getText()),
                    Integer.parseInt(txtCodigoCliente.getText()), Integer.parseInt(txtCuantoLlevara.getText()), 
                    Integer.parseInt(lblValorVenta.getText()),  txtFechaCompra.getText(), txtHora.getText());
 
            int Ac = Integer.parseInt(lblCantidadBodega.getText())-(Integer.parseInt(txtCuantoLlevara.getText()));
            con.ActualizarBodega(Ac, Integer.parseInt(txtCodigoArticulo.getText()));
            limpiar();
        }
            
            
       
    }//GEN-LAST:event_BtnregistrarcActionPerformed

    private void btnBuscarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarEActionPerformed
         if(txtCodigoE.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Para buscar un empleado debe ingresar el codigo del empleado");
        }
        else{
            Conectar con =new Conectar();
            con.getConecxion();
            con.buscarEmpleado(Integer.parseInt(txtCodigoE.getText()));
            txtCodigoE.setText(txtCodigoE.getText());
            txtNombreE.setText(con.getNomEmpleado()+" "+con.getApeEmpleado());
            
        }
    }//GEN-LAST:event_btnBuscarEActionPerformed

