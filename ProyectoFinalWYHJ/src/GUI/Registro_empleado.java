    public long E(){
        DateTimeFormatter fecha=DateTimeFormatter.ofPattern("yyyy-MM-dd");
        String fInicial=txtnacimiento.getText();
        String fFinal=fecha.format(LocalDateTime.now());
        LocalDate fechaInicial=LocalDate.parse(fInicial);
        LocalDate fechaFinal=LocalDate.parse(fFinal);
        long edadr=ChronoUnit.YEARS.between(fechaInicial, fechaFinal);
        txtedad.setText(String.valueOf(edadr));
        return edadr;
    }

