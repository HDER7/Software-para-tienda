    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        txtedad.setText(String.valueOf(E()));
        if(txtcodigo.getText().equals("")|| txtnombre.getText().equals("")|| txtapellido.getText().equals("") || txtnacimiento.getText().equals("") || txttel.getText().equals("") || String.valueOf(txtpass.getPassword()).equals("")){
            JOptionPane.showMessageDialog(null,"Complete todos los campos para registrar el empleado");
        }
        else{
            Conectar con =new Conectar();
            con.getConecxion();
            con.insertaEmpleado(Integer.parseInt(txtcodigo.getText()),txtnombre.getText(), txtapellido.getText(),Integer.parseInt(txtedad.getText()),txtnacimiento.getText(),Long.parseLong(txttel.getText()),String.valueOf(txtpass.getPassword()));
            limpiar();
        }
    }//GEN-LAST:event_btnregistrarActionPerformed

    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        if(txtcodigo.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Para buscar un empleado debe ingresar el codigo del empleado");
        }
        else{
            Conectar con =new Conectar();
            con.getConecxion();
            con.buscarEmpleado(Integer.parseInt(txtcodigo.getText()));
            txtnombre.setText(con.getNomEmpleado());
            txtapellido.setText(con.getApeEmpleado());
            txtedad.setText(String.valueOf(con.getEdadEmpleado()));
            txtnacimiento.setText(con.getFnEmpleado());
            txtpass.setText(con.getpassEm());
            txttel.setText(String.valueOf(con.getTelEmpleado()));
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        if(txtcodigo.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Para eliminar un empleado debe ingresar el codigo del empleado");
        }
        else{
            if (JOptionPane.showConfirmDialog(null, "Seguro de eliminar el empleado", "Eliminar empleado",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                Conectar c= new Conectar();
                c.getConecxion();
                c.eliminarEmpleado(Integer.parseInt(txtcodigo.getText()));
                limpiar();
            }   
        }
    }//GEN-LAST:event_btneliminarActionPerformed

